"""
Patch OLP-OPS-001_Operations_Manual.docx:
- Remove emoji characters from all text
- Fix conda env name bde → olist
- Add Dagster orchestration content to section 4
"""
import re
from docx import Document
from docx.shared import Pt, RGBColor
from docx.oxml.ns import qn
from docx.oxml import OxmlElement

EMOJI_RE = re.compile(
    "["
    "\U0001F000-\U0001FFFF"
    "\U00002600-\U000027BF"
    "\U0000FE0F"
    "\U000020D0-\U000020FF"
    "\U00002702-\U000027B0"
    "]+",
    flags=re.UNICODE,
)

# Additional common emojis as literal chars
SPECIFIC = ["📋", "📅", "🔖", "📦", "⚙️", "⚙", "✅", "❌", "⚠️"]


def strip_emoji(text):
    for e in SPECIFIC:
        text = text.replace(e, "")
    text = EMOJI_RE.sub("", text)
    return text


def clean_runs(para):
    for run in para.runs:
        if run.text:
            cleaned = strip_emoji(run.text)
            # Fix field labels: remove leftover whitespace before colon
            cleaned = re.sub(r'\s{2,}:', ':', cleaned)
            run.text = cleaned


def fix_env(para):
    for run in para.runs:
        if run.text:
            run.text = run.text.replace("bde-environment.yml", "olist-environment.yml")
            run.text = run.text.replace("conda activate bde", "conda activate olist")



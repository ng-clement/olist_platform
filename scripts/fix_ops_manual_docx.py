def fix_doc():
    doc = Document("docs/OLP-OPS-001_Operations_Manual.docx")

    structure_replaced = False
    orchestration_found = False

    for para in doc.paragraphs:
        # Strip emojis from all paragraphs
        clean_runs(para)
        fix_env(para)

        txt = para.text.strip()

        # Replace the project structure block
        if not structure_replaced and "olist_platform/" in txt and ("PROJECT_ROOT" in txt or "project root" in txt.lower()):
            for run in para.runs:
                run.text = ""
            if para.runs:
                para.runs[0].text = NEW_STRUCTURE
                para.runs[0].font.name = "Courier New"
                para.runs[0].font.size = Pt(8)
            structure_replaced = True

        # Update the conda install section
        if "conda env create" in txt and "bde" in txt.lower():
            for run in para.runs:
                run.text = run.text.replace("bde-environment.yml", "olist-environment.yml")
                run.text = run.text.replace("conda activate bde", "conda activate olist")

        # Update Section 4 to add Dagster context
        if txt.startswith("4.") and "Running" in txt and "Application" in txt:
            orchestration_found = True

        if orchestration_found and not structure_replaced and "Every command below is run from" in txt:
            # Append Dagster information to this paragraph
            for run in para.runs:
                pass
            # Add a new run with Dagster content
            existing = para.text
            for run in para.runs:
                run.text = ""
            if para.runs:
                para.runs[0].text = (
                    existing.strip() + "\n\n"
                    "Orchestration (Dagster):\n"
                    "    DAGSTER_HOME=$(pwd) dagster dev -m orchestration.dagster\n"
                    "For full Dagster setup and launch options, see Section 3.5 below "
                    "and docs/dagster_orchestration.md."
                )

    # Also fix tables
    for tbl in doc.tables:
        for row in tbl.rows:
            for cell in row.cells:
                for para in cell.paragraphs:
                    clean_runs(para)
                    fix_env(para)

    # ── Find Section 4 and inject Dagster subsection ──────────────────────────
    # Strategy: find "Section 4" heading paragraph, then find the following
    # "Every command below" paragraph and insert Dagster block right after it.
    paras = doc.paragraphs
    insert_idx = None
    for i, para in enumerate(paras):
        if "Every command below is run from" in para.text:
            insert_idx = i
            break

    if insert_idx is not None:
        # We'll append a paragraph after the found paragraph
        ref_para = paras[insert_idx]
        ref_el = ref_para._element
        parent = ref_el.getparent()
        idx = list(parent).index(ref_el)

        # Create heading paragraph
        h_para = doc.add_paragraph()
        h_run = h_para.add_run("Pipeline Orchestration — Dagster")
        h_run.bold = True
        h_run.font.size = Pt(12)
        h_run.font.color.rgb = RGBColor(0x0D, 0x3B, 0x66)
        h_el = h_para._element
        parent.remove(h_el)
        parent.insert(idx + 1, h_el)

        # Create content paragraph
        d_para = doc.add_paragraph()
        d_run = d_para.add_run(DAGSTER_CONTENT)
        d_run.font.size = Pt(9.5)
        d_run.font.name = "Courier New"
        d_el = d_para._element
        parent.remove(d_el)
        parent.insert(idx + 2, d_el)

    doc.save("docs/OLP-OPS-001_Operations_Manual.docx")
    print("Saved docs/OLP-OPS-001_Operations_Manual.docx")


if __name__ == "__main__":
    fix_doc()
